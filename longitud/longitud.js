let metro = parseFloat(prompt("Ingrese longitud"));
let medidaoriginal = metro 
let centimetro = metro * 100 
let medidanueva = centimetro
switch (medidaoriginal) {

    case medidaoriginal:
        console.log("Medida original: metros",medidaoriginal);
        break;
    default:
        console.log("Longitud fuera de limite");
        break;
}
switch (medidanueva) {
    case medidanueva:
        console.log("Medida destinataria: centimetros",medidanueva);
    break;

    default:
        console.log("Longitud fuera de limite");
        break;
}